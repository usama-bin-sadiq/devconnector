const mongoose = require("mongoose");
const UserSchema = new mongoose({
  name: {
    type: String,
    requested: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  avatar: {
    type: String,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

module.exports = User = mongoose.Schema("user", UserSchema);
